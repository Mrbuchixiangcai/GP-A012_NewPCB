uint16_t mLevle_Map_Tab[6][10];
uint16_t sLevle_Map_Tab[6][10];
uint16_t cntLevel_none;
void LevelMode_AudioDeal(void)
{
 uint8_t  x;	
 uint32_t _sum;			
 uint16_t tmp_data;	
 static uint8_t y[10];
 for(x=0;x<10;x++) 	
 {	
  tmp_data=AdcAudio_Data[x];	
  if(tmp_data>mLevle_Map_Tab[y[x]][x])
  {
	sLevle_Map_Tab[y[x]][x]=1;
    if(y[x]>0)
     y[x]--;
    mLevle_Map_Tab[y[x]][x]=tmp_data;
  }
  else
  {
	sLevle_Map_Tab[y[x]][x]=0;  
    if(y[x]<5)
     y[x]++;
    mLevle_Map_Tab[y[x]][x]=tmp_data;  
	sLevle_Map_Tab[y[x]][x]=1;
  }  
 } 
 for(_sum=0,x=0;x<10;x++)
 {
  _sum+=AdcAudio_Data[x];
 }  
 if((_sum/10)<=5) 
 {
  if(cntLevel_none<100)  
   cntLevel_none++;
 }
 else if((_sum/10)>=40) 
 {
  cntLevel_none=0;	
 }   
}
void Level_Fire_Mode(void)
{
 uint8_t x,j,ff;
 static uint8_t	cntDelay;
 if(FireMode_c!=FireMode)
 { 
  FireMode_c=FireMode;
  cntDelay=0;
  cntLevel_none=100;	 
  for(x=0;x<10;x++)	
  {
   for(j=0;j<6;j++)
   {
	  Fire_Data_H[j][x]=0;   
	  mLevle_Map_Tab[j][x]=0;    
	  sLevle_Map_Tab[j][x]=0;  
   }
  }	 
 }	
 LevelMode_AudioDeal();		
 for(ff=0;ff<2;ff++)	
 { 
  for(x=0;x<10;x++)	
  {
   for(j=0;j<6;j++)
   {
    if(sLevle_Map_Tab[j][x])  
     Fire_Data_H[j][x]=30;
    else if(Fire_Data_H[j][x])
     Fire_Data_H[j][x]--;		
   }
  }
 }
 if(cntDelay<10)
 {
  cntDelay++;  
  for(x=0;x<10;x++)	
   for(j=0;j<6;j++)	 
    Fire_Data_H[j][x]=0;
 }	 
 if(cntLevel_none>=100) 
 {
  s_Level_Fire_Mode();
  LoadData_to_MC96F_V();	 
 }
 else
 {
  LoadData_to_MC96F_H();	 
 }	
}
