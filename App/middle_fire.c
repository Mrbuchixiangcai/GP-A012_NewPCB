uint8_t mMiddle_shift_cnt; 
uint8_t mMiddle_index_run;
uint8_t mMiddlel_index_max;
uint8_t mMiddle_index_min;
void h_Middle_Fire_Mode(void)
{
 u8 i,j,k;
 if(FireMode_c!=FireMode)
 { 
  FireMode_c=FireMode;
  mMiddle_mirror=0;	 
  mMiddle_shift_cnt=0;	 
  mMiddle_index_run=0;
  mMiddle_speed_cnt=200;	 
  for(i=0;i<12;i++)
  {
    for(j=0;j<5;j++)
      Fire_Data_V[i][j]=0;
  }
 }		
 if(++mMiddle_speed_cnt>12)
 {		
  mMiddle_speed_cnt=0;
  if(++mMiddle_index_run>=13)
  {
   mMiddle_index_run=0; 
   if(++mMiddle_shift_cnt>=5)
   {mMiddle_shift_cnt=0;}{mMiddle_mirror=~mMiddle_mirror;}   
  }
  k=mMiddle_shift_cnt;
  for(i=0;i<12;i++)
  {
    for(j=0;j<5;j++)
	{
	 if(!mMiddle_mirror)
      Fire_Data_V[i][j]=Middle_Fire_tab_array[mMiddle_index_run][i][k]*2;///2*2;
     else	
      Fire_Data_V[i][j]=Middle_Fire_tab_array[mMiddle_index_run][i][4-k]*2;//*2;	
     if(++k>=5)
	  k=0; 
    }   
  }
 }
 LoadData_to_MC96F_V();
}
